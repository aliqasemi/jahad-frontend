<template>
    <v-row class="item">
        <v-col lg="2">
            {{ index + 1 }}
        </v-col>
        <v-col lg="3">
            {{ item.title }}
        </v-col>
        <v-col lg="2">
            {{ item.user.firstname + ' ' + item.user.lastname }}
        </v-col>
        <v-col lg="5" style="text-align: left">
            <router-link :to="{name:'EditStep',  params: { step_id: item.id },}" style="text-decoration: none;">
                <v-tooltip top>

                    <template v-slot:activator="{ on,attrs }">

                        <v-btn
                            slot="activator"
                            v-bind="attrs"
                            v-on="on"
                        >
                            <v-icon dark>fa-edit</v-icon>
                        </v-btn>

                    </template>
                    <span>ویرایش</span>
                </v-tooltip>
            </router-link>
            <v-tooltip top>
                <template v-slot:activator="{ on , attrs}">
                    <v-btn
                        slot="activator"
                        v-bind="attrs"
                        v-on="on"
                        @click.native="deleteDialog = true"
                    >
                        <v-icon dark>fa-trash</v-icon>
                    </v-btn>
                </template>
                <span>حذف</span>
            </v-tooltip>
            <delete-modal v-model="deleteDialog" @action="deleteService(item.id)"/>
        </v-col>
    </v-row>
</template>

<script>
import DeleteModal from "../GeneralComponent/deleteModal";
import {mapActions} from "vuex";

export default {
    name: "Item",
    props: {
        item: {default: null},
        index: {default: 0},
    },
    components: {
        DeleteModal
    },
    data() {
        return {
            deleteDialog: false
        }
    },
    methods: {
        ...mapActions("service", ['removeService']),
        async deleteService(id) {
            let response;
            response = await this.removeService(id);
            if (!(response instanceof Error)) {
                this.deleteDialog = false;
            }
        },
    }
}
</script>

